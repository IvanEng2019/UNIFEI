 /* Escreva um algoritmo para ler dois valores inteiros, efetuar e mostrar o
 resultado das operações de adição, subtração e multiplicação do primeiro número pelo segundo.
 Considere que o segundo número é sempre diferente de 0*/

 int main ()
 {
     int a,b;
     printf("Entre com o primeiro e segundo valor:");
     scanf("%d %d",&a,&b);
     printf("\nA adição é: %d", a+b, "\n");
     printf("\nA subtração é:%d", a-b, "\n" );
     printf("\nA multiplicação é %d:", a*b, "\n");
     return 0;
 }

